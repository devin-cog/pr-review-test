def greet(name):
      """Greet someone by name."""
      print(f"Hello, {name}!")


def add(a, b):
      """Add two numbers."""
      return a + b


def divide(a, b):
      """Divide a by b."""
      return a / b


if __name__ == "__main__":
      greet("World")
      print(add(2, 3))
      print(divide(10, 0))
